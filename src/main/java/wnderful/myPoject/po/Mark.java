package wnderful.myPoject.po;

public class Mark {
    private String name;
    private int endMark;
    private int[] x;
    private int[] y;

    public Mark() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int[] getX() {
        return x;
    }

    public void setX(int[] x) {
        this.x = x;
    }

    public int[] getY() {
        return y;
    }

    public void setY(int[] y) {
        this.y = y;
    }

    public int getEndMark() {
        return endMark;
    }

    public void setEndMark(int endMark) {
        this.endMark = endMark;
    }
}
